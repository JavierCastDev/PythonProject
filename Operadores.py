print(5 > 2) # Mayor que

print(5 < 10)# Menor que

print(5 >= 5)# Mayor o igual

print(5 <= 5)# Menor o igual

print(5 == 5)# Igual

print(5 != 6)# Diferente de

print(5 > 2) and (5 >1)# Evalua dos condiciones y devuelve True solo si las dos se cumplen

print(5 > 2) or (5 > 8)# Evalua dos condiciones y devuelve True si una o las dos codiciones se cumplen

print(not(5 >2))# Devuelve el contrario, si es True muestra False